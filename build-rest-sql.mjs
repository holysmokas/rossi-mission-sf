// build-rest-sql.mjs
// Reads supabase-export/tables/*.json and writes one D1 SQL file.
//
//   node build-rest-sql.mjs
//
// Then:
//   npx wrangler d1 execute rossi --remote --file=supabase-export/update-rest.sql

import { readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';

const TABLES_DIR = './supabase-export/tables';
const OUT = './supabase-export/update-rest.sql';

const rewriteUrl = (url) => {
  if (typeof url !== 'string') return url;
  const m = url.match(/\/storage\/v1\/object\/public\/(.+)$/);
  return m ? `/media/${m[1]}` : url;
};
const esc = (v) => {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'boolean') return v ? '1' : '0';
  if (Array.isArray(v) || typeof v === 'object') {
    return `'${JSON.stringify(v).replace(/'/g, "''")}'`;
  }
  return `'${String(v).replace(/'/g, "''")}'`;
};
const toUnix = (v) => {
  if (v === null || v === undefined) return null;
  if (typeof v === 'number') return v > 1e12 ? Math.floor(v / 1000) : v;
  const d = new Date(v);
  return isNaN(d) ? null : Math.floor(d.getTime() / 1000);
};
const load = (name) => JSON.parse(readFileSync(join(TABLES_DIR, name + '.json'), 'utf8'));

const out = [];
const log = (s) => { out.push(s); };

log('-- Generated by build-rest-sql.mjs');
log('-- Run: npx wrangler d1 execute rossi --remote --file=supabase-export/update-rest.sql');
log('');

// ─── orders ─────────────────────────────────────────────────────────────────
const orders = load('orders');
log(`-- orders: ${orders.length} rows (INSERT OR REPLACE on UUID id)`);
const orderCols = [
  'id', 'square_order_id', 'square_payment_id', 'status', 'items',
  'item_count', 'total_cents', 'currency', 'customer_name', 'customer_email',
  'shipping_address', 'square_receipt_url', 'notified', 'paid_at', 'created_at'
];
for (const o of orders) {
  const row = {
    id: o.id,
    square_order_id: o.square_order_id,
    square_payment_id: o.square_payment_id,
    status: o.status,
    items: Array.isArray(o.items) ? o.items : [],
    item_count: o.item_count ?? 0,
    total_cents: o.total_cents ?? 0,
    currency: o.currency || 'USD',
    customer_name: o.customer_name,
    customer_email: o.customer_email,
    shipping_address: o.shipping_address || null,
    square_receipt_url: o.square_receipt_url,
    notified: o.notified ? 1 : 0,
    paid_at: toUnix(o.paid_at),
    created_at: toUnix(o.created_at),
  };
  log(`INSERT OR REPLACE INTO orders (${orderCols.join(', ')}) VALUES (${orderCols.map(c => esc(row[c])).join(', ')});`);
}
log('');

// ─── gallery_images (wipe + reload; id is INTEGER AUTOINCREMENT in D1) ─────
const gallery = load('gallery_images');
log(`-- gallery_images: ${gallery.length} rows (wipe + reload)`);
log('DELETE FROM gallery_images;');
const galCols = ['image_url', 'storage_path', 'file_name', 'title', 'artist', 'sort_order', 'active', 'created_at'];
for (const g of gallery) {
  const row = {
    image_url: rewriteUrl(g.image_url),
    storage_path: g.storage_path,
    file_name: g.file_name,
    title: g.title,
    artist: g.artist,
    sort_order: g.sort_order ?? 0,
    active: g.active ? 1 : 0,
    created_at: toUnix(g.created_at),
  };
  log(`INSERT INTO gallery_images (${galCols.join(', ')}) VALUES (${galCols.map(c => esc(row[c])).join(', ')});`);
}
log('');

// ─── showcase_images (same pattern; no title/artist columns) ───────────────
const showcase = load('showcase_images');
log(`-- showcase_images: ${showcase.length} rows (wipe + reload)`);
log('DELETE FROM showcase_images;');
const shoCols = ['image_url', 'storage_path', 'file_name', 'sort_order', 'active', 'created_at'];
for (const s of showcase) {
  const row = {
    image_url: rewriteUrl(s.image_url),
    storage_path: s.storage_path,
    file_name: s.file_name,
    sort_order: s.sort_order ?? 0,
    active: s.active ? 1 : 0,
    created_at: toUnix(s.created_at),
  };
  log(`INSERT INTO showcase_images (${shoCols.join(', ')}) VALUES (${shoCols.map(c => esc(row[c])).join(', ')});`);
}
log('');

// ─── contact_messages ──────────────────────────────────────────────────────
const messages = load('contact_messages');
log(`-- contact_messages: ${messages.length} rows (wipe + reload)`);
log('DELETE FROM contact_messages;');
// D1 schema doesn't have a `read` column; safe to drop. created_at maps from submitted_at.
const msgCols = ['name', 'email', 'message', 'submitted_at', 'created_at'];
for (const m of messages) {
  const ts = toUnix(m.submitted_at);
  const row = {
    name: m.name,
    email: m.email,
    message: m.message,
    submitted_at: ts,
    created_at: ts,
  };
  log(`INSERT INTO contact_messages (${msgCols.join(', ')}) VALUES (${msgCols.map(c => esc(row[c])).join(', ')});`);
}
log('');

writeFileSync(OUT, out.join('\n'));
console.log(`Wrote ${OUT}`);
console.log(`  orders: ${orders.length}`);
console.log(`  gallery_images: ${gallery.length}`);
console.log(`  showcase_images: ${showcase.length}`);
console.log(`  contact_messages: ${messages.length}`);
